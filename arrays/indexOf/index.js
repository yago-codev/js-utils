const arr = ['🍎', '🍐', '🍊', '🍌']

// a função indexOf será utilizada para buscarmos o índice
// de um determinado elemento.
// OBS: quando o valor não for encontrado no array, será retornado -1
const indiceLaranja = arr.indexOf('🍊')

console.log(indiceLaranja) // 2

const indiceBanana = arr.indexOf('🍌')

console.log(indiceBanana) // 3
